import Foundation

/// A utility class to perform basic mathematical operations.
public final class Addition {
    
    /// Initializes a new instance of `Addition`.
    public init() {}
    
    /// Adds two integers and returns the result.
    ///
    /// - Parameters:
    ///   - a: The first integer.
    ///   - b: The second integer.
    /// - Returns: The sum of `a` and `b`.
    public func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
}

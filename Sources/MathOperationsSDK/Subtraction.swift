import Foundation

/// A utility class to perform basic mathematical operations.
public final class Subtraction {
    
    /// Initializes a new instance of `Subtraction`.
    public init() {}
    
    /// Adds two integers and returns the result.
    ///
    /// - Parameters:
    ///   - a: The first integer.
    ///   - b: The second integer.
    /// - Returns: The Subtract of `a` and `b`.
    public func subtract(_ a: Int, _ b: Int) -> Int {
        return a - b
    }
}
